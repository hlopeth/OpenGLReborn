SkyBox::SkyBox(CubeTexture _texture): texture(_texture)
{
    shader = ShaderProgram("skyBoxVertex.glsl", "skyBoxFragment.glsl");
    GLuint VBO;
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);

    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);

    glBufferData(GL_ARRAY_BUFFER, 108 * sizeof(GLfloat), &vertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(GLfloat), (void*)0);
    glBindVertexArray(0);
}

void SkyBox::draw(RenderData& renderData) const
{
    shader.use();
    mat4 mvp = renderData.camera.getProjection() *
        glm::mat4(glm::mat3(renderData.camera.getView()));
    shader.setUniform("mvp", mvp);
    glActiveTexture(GL_TEXTURE0);
    texture.bind();
    glBindVertexArray(VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}
